# Most DBMS tools will allow us to set this constraint, or limitation, on columns in our tables.
# So if we tried to enter a record with a value that's already in the table, the database will reject it until the information is changed to be unique
# Unique values can also be used as keys.
# A key is a value we can use to refer to only one specific row or record.
# A primary key is the most important key in a table, though there can be others as well.
# Composite Key : Two or more fields taken together to act as a unique identifier .
# The foreign key is what a primary key from one table is called when it's referenced in another table.
